#include <unistd.h>
void  ft_swap(int *a, int *b);
{
	int temp = *a;
	*a = *b;
	*b =temp;
	assert( *a == 42);
	assert (*b ==42);
}



int  main()
{

	int a = 42;
	
	int b = 42;
	ft_swap(&a, &b);
	assert (*a == 42);
	assert(*b ==42);
}
